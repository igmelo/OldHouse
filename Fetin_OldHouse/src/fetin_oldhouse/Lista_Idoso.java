    private void jList1ValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_jList1ValueChanged

        int x, y;
        DAOIdoso daoido = new DAOIdoso();
        DAOResponsavel daoresp = new DAOResponsavel();
        ArrayList<Idoso> ido = daoido.selecionaTodos();
        ArrayList<Responsavel> resp = daoresp.selecionaTodos();
        auxu = jList1.getSelectedValue();
        nome.setText(null);
        cidade.setText(null);
        datai.setText(null);
        cpf.setText(null);
        rg.setText(null);
        obs.setText(null);
        estado.setSelectedItem(null);
        casa.setSelectedItem(null);

        for (Idoso user : ido) {

            if (user.getNome().equals(auxu)) {

                nome.setText(user.getNome());
                cidade.setText(user.getCidade());
                datai.setText(user.getData_nasc());
                cpf.setText(user.getCpf());
                rg.setText(user.getRg());
                obs.setText(user.getObservacoes());
                estado.setSelectedItem(user.getEstado());
                tag.setText(user.getTag_idoso());
                casa.setSelectedIndex(user.getCasa() - 1);
                if (user.getFoto() == null) {

                } else {

                    File file = new File(user.getFoto());
                    ImageIcon foto = new ImageIcon(file.getPath());
                    Image foto1 = foto.getImage().getScaledInstance(155, 170, Image.SCALE_DEFAULT);
                    foto_perfil.setIcon(new ImageIcon(foto1));
                }

                x = user.getResponsavel_id();
                for (Responsavel user1 : resp) {

                    y = user1.getId_resp();
                    if (x == y) {

                        nomer.setText(user1.getNome());
                        cidader.setText(user1.getCidade());
                        bairro.setText(user1.getBairro());
                        rgr.setText(user1.getRg());
                        cpfr.setText(user1.getCpf());
                        rua.setText(user1.getRua());
                        num.setText(user1.getNumero());
                        datar.setText(user1.getData_nasc());
                        tel.setText(user1.getTelefone());
                        estador.setSelectedItem(user1.getEstado());
                        break;
                    }
                }
            }
        }
    }//GEN-LAST:event_jList1ValueChanged

