    private void excluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_excluirActionPerformed

        boolean selecionado = false;
        for (int i = 0; i < jList1.getModel().getSize(); i++) {

            if (jList1.isSelectedIndex(i)) {

                int opcao = JOptionPane.showConfirmDialog(rootPane, "Deseja mesmo excluir os dados da pessoa cadastrada?",
                        "Atenção", JOptionPane.YES_NO_OPTION, JOptionPane.INFORMATION_MESSAGE);
                if (opcao == 0) {

                    DAOIdoso aux = new DAOIdoso();
                    ArrayList<Idoso> idosos = aux.selecionaTodos();

                    for (Idoso user : idosos) {

                        if (user.getNome().equals(auxu)) {

                            aux.delete(auxu);
                            nome.setText(null);
                            nomer.setText(null);
                            cidade.setText(null);
                            cidader.setText(null);
                            bairro.setText(null);
                            rua.setText(null);
                            rg.setText(null);
                            rgr.setText(null);
                            cpf.setText(null);
                            cpfr.setText(null);
                            estado.setSelectedIndex(0);
                            estador.setSelectedIndex(0);
                            num.setText(null);
                            tel.setText(null);
                            datar.setText(null);
                            obs.setText(null);
                            foto_perfil.setIcon(null);
                            tag.setText(null);
                        }
                    }
                    ((DefaultListModel) jList1.getModel()).remove(jList1.getSelectedIndex());
                }

            }else{
                
                JOptionPane.showMessageDialog(null, "Nenhum dado selecionado!");
            }
        }


    }//GEN-LAST:event_excluirActionPerformed

