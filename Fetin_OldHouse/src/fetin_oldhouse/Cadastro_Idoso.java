    private void jList1ValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_jList1ValueChanged

        jPanel4.setVisible(true);
        novoresp.setSelected(false);
        DAOResponsavel daoresp = new DAOResponsavel();
        ArrayList<Responsavel> resp = daoresp.selecionaTodos();
        auxu = jList1.getSelectedValue();
        for (Responsavel user : resp) {

            if (user.getNome().equals(auxu)) {
                nomer.setText(user.getNome());
                cidader.setText(user.getCidade());
                bairro.setText(user.getBairro());
                rgr.setText(user.getRg());
                cpfr.setText(user.getCpf());
                rua.setText(user.getRua());
                num.setText(user.getNumero());
                datar.setText(user.getData_nasc());
                tel.setText(user.getTelefone());
                estador.setSelectedItem(user.getEstado());

            }
        }


    }//GEN-LAST:event_jList1ValueChanged

