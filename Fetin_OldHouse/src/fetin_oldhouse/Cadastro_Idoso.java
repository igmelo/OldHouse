    private void salvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvarActionPerformed

        int chave = 0;
        boolean selecionado = true;
        Responsavel resp = new Responsavel();
        DAOResponsavel daoresp = new DAOResponsavel();
        Idoso ido = new Idoso();
        int qnt = jList1.getModel().getSize();

        if (novoresp.isSelected()) {

            resp.setBairro(bairro.getText());
            resp.setCidade(cidader.getText());
            resp.setCpf(cpfr.getText());
            resp.setRg(rgr.getText());
            resp.setEstado((String) estador.getSelectedItem());
            resp.setNome(nomer.getText());
            resp.setData_nasc(datar.getText());
            resp.setNumero(num.getText());
            resp.setRua(rua.getText());
            resp.setTelefone(tel.getText());
            daoresp.respaux = resp;
            daoresp.insere();

            ido.setCidade(cidadei.getText());
            ido.setCpf(cpfi.getText());
            ido.setData_nasc(datai.getText());
            ido.setEstado((String) estadoi.getSelectedItem());
            ido.setFoto(dir);
            ido.setNome(nomei.getText());
            ido.setObservacoes(obs.getText());
            ido.setRg(rgi.getText());
            ido.setCasa(Integer.parseInt((String)casa.getSelectedItem()));
            ido.setResponsavel_id(daoresp.retorna_id());
            ido.setTag_idoso(txt_tag.getText());
            DAOIdoso daoi = new DAOIdoso();
            daoi.idoaux = ido;
            daoi.insere();
        } else {

            auxu = jList1.getSelectedValue();
            ArrayList<Responsavel> responsaveis = daoresp.selecionaTodos();

            for (Responsavel user : responsaveis) {

                if (auxu.equals(user.getNome())) {

                    chave = user.getId_resp();
                    break;
                }
            }

            ido.setResponsavel_id(chave);
            ido.setCidade(cidadei.getText());
            ido.setCpf(cpfi.getText());
            ido.setData_nasc(datai.getText());
            ido.setEstado((String) estadoi.getSelectedItem());
            ido.setFoto(dir);
            ido.setNome(nomei.getText());
            ido.setObservacoes(obs.getText());
            ido.setRg(rgi.getText());
            ido.setCasa(Integer.parseInt((String)casa.getSelectedItem()));
            ido.setTag_idoso(txt_tag.getText());
            DAOIdoso daoi = new DAOIdoso();
            daoi.idoaux = ido;
            daoi.insere();
        }

        JOptionPane.showMessageDialog(null, "Cadastro realizado com sucesso!");
        Controller C = new Controller();
        dispose();
        C.criarMenu();
    }//GEN-LAST:event_salvarActionPerformed

